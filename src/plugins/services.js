export const getColor = (ecurie) => {
    switch (ecurie) {
        case 'Red Bull' :
            return 'indigo darken-2'
        case 'Mercedes' :
            return 'teal accent-5'
        case 'Ferrari' :
            return 'red'
        case 'McLaren' :
            return 'orange darken-1'
        case 'AlphaTauri' :
            return 'light-blue darken-4'
        case 'Alfa Romeo' :
            return 'red  lighten-1'
        case 'Haas F1 Team' :
            return 'blue-grey lighten-1'
        case 'Alpine F1 Team' :
            return 'indigo darken-2'
        case 'Williams' :
            return 'blue accent-4'
        case 'Aston Martin' :
            return 'teal lighten-1'
        case 'Racing Point' :
            return 'purple accent-1'
        case 'Renault' :
            return 'amber darken-1'
    }

}

